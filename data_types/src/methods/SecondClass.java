package methods;

public class SecondClass {
     
	public static void iAmStaticSecond( ) {
		System.out.println("Hello static from second class");
	}
	
	public static void iAmStaticSecond(String name) {
		System.out.println("Hello static from second class with name " + name);
	}
	
	public void iAmNotStatSecond() {
		System.out.println("Hello non static from second class with name ");
	}
	
	
}
